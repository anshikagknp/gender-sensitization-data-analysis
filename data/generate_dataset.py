"""
generate_dataset.py
====================

Generates a FULLY SYNTHETIC dataset inspired by the aggregate (published)
percentages reported in the dissertation:

  "Perspectives on Gender Sensitization: A Kanpur-based comprehensive study"
  Anshika Gupta, MA Political Science, Christ Church College, Kanpur (2023-24)

IMPORTANT:
- This script does NOT use, store, or reproduce any real respondent data.
- The dissertation only reports aggregate percentages (e.g., "77% had heard
  of the term Gender Sensitization"); it contains no individual-level
  records. No such records were available to this script in any case.
- All 1000 rows produced are randomly generated by this script using
  probability targets loosely derived from the published percentages, plus
  invented correlations between variables added purely for demonstration
  purposes (e.g., for teaching dashboards, ML pipelines, or analytics
  practice). These correlations were NOT observed in or claimed by the
  original study.
- Do not present this file as real survey data in any academic, journalistic,
  or policy context.

See DATA_GENERATION.md for full documentation.
"""

import csv
import random

random.seed(42)

N = 1000

# ---------------------------------------------------------------------------
# Helper
# ---------------------------------------------------------------------------
def bern(p):
    """Return True with probability p."""
    return random.random() < p


def pick_weighted(options_probs):
    """options_probs: list of (value, prob). Probabilities should sum to ~1."""
    r = random.random()
    cum = 0.0
    for val, p in options_probs:
        cum += p
        if r <= cum:
            return val
    return options_probs[-1][0]


# ---------------------------------------------------------------------------
# Demographics (Section 4.1 of dissertation)
# ---------------------------------------------------------------------------
AGE_GROUPS = [
    ("Less than 18", 0.02),
    ("18-25", 0.86),
    ("26-35", 0.10),
    ("More than 35", 0.02),
]

EDU_LEVELS = [
    ("High school or equivalent", 0.10),
    ("Bachelor's degree", 0.66),
    ("Master's degree", 0.24),
    ("Doctoral or post-doctoral degree", 0.00),  # none reported in study
]

rows = []

for i in range(1, N + 1):
    respondent_id = f"R{i:04d}"

    gender = pick_weighted([("Male", 0.53), ("Female", 0.47)])
    age_group = pick_weighted(AGE_GROUPS)
    education = pick_weighted(EDU_LEVELS)

    # Small synthetic skew: higher education -> slightly higher baseline
    # awareness probability (invented relationship, for demo purposes only).
    edu_bonus = {
        "High school or equivalent": -0.07,
        "Bachelor's degree": 0.00,
        "Master's degree": 0.08,
        "Doctoral or post-doctoral degree": 0.15,
    }[education]

    # Slight synthetic age effect: older respondents marginally more likely
    # to have received formal training (invented, for demo purposes only).
    age_bonus = {
        "Less than 18": -0.10,
        "18-25": 0.00,
        "26-35": 0.06,
        "More than 35": 0.10,
    }[age_group]

    # --- 4.2 Gender Sensitization Awareness --------------------------------
    p_heard_term = min(max(0.77 + edu_bonus, 0.05), 0.97)
    heard_term = bern(p_heard_term)

    gender_binary_view = pick_weighted(
        [("Binary", 0.384), ("Non-binary", 0.394), ("Unsure", 0.222)]
    )

    p_training = min(max(0.398 + edu_bonus + age_bonus, 0.05), 0.95)
    # People who never heard the term are far less likely to report training
    if not heard_term:
        p_training *= 0.4
    received_training = bern(p_training)

    support_school_curriculum = pick_weighted(
        [("Yes, definitely", 0.814), ("Maybe", 0.103), ("No, not required", 0.083)]
    )
    support_workplace_training = pick_weighted(
        [("Yes, definitely", 0.74), ("Maybe", 0.15), ("No, not required", 0.11)]
    )
    sensitization_helps_relationships = pick_weighted(
        [("Yes", 0.75), ("Maybe", 0.177), ("No", 0.073)]
    )

    # --- 4.3 Gender Stereotypes ---------------------------------------------
    # Synthetic relationship: respondents who never heard the term / had no
    # training are somewhat more likely to agree with traditional stereotypes.
    stereotype_shift = (0.10 if not heard_term else 0.0) + (
        0.08 if not received_training else 0.0
    )

    def stereo(agree_p, disagree_p, neutral_p):
        agree_p = min(max(agree_p + stereotype_shift, 0.02), 0.95)
        remaining = 1 - agree_p
        # split remaining between disagree/neutral keeping original ratio
        total_dn = disagree_p + neutral_p
        d = remaining * (disagree_p / total_dn) if total_dn > 0 else remaining
        n = remaining - d
        return pick_weighted([("Agree", agree_p), ("Disagree", d), ("Neutral", n)])

    men_rational_women_emotional = stereo(0.333, 0.434, 0.232)
    women_bad_drivers = stereo(0.13, 0.66, 0.21)
    okay_for_men_to_cry = stereo(0.82, 0.13, 0.05)  # "Agree" here = progressive view
    men_strong_women_weak = stereo(0.16, 0.75, 0.09)
    women_better_housekeepers = stereo(0.21, 0.62, 0.17)
    gentle_men_less_masculine = stereo(0.232, 0.677, 0.091)

    # --- 4.4 Gender Roles -----------------------------------------------------
    childcare_only_mother = pick_weighted([("Yes", 0.237), ("No", 0.763)])
    woman_home_man_provider = pick_weighted([("Yes", 0.347), ("No", 0.653)])
    chores_divided_equally = pick_weighted(
        [("Yes, definitely", 0.939), ("No, the woman should manage both", 0.061)]
    )

    # --- 4.5 Gender Equality ---------------------------------------------------
    equal_pay_support = pick_weighted([("Yes", 0.908), ("No", 0.092)])
    gender_diversity_support = pick_weighted(
        [("Yes", 0.704), ("No", 0.133), ("Unsure", 0.163)]
    )
    discrimination_observed = pick_weighted(
        [("Yes", 0.612), ("No", 0.245), ("Unsure", 0.143)]
    )
    gender_neutral_language_support = pick_weighted(
        [("Yes", 0.697), ("Maybe", 0.212), ("No", 0.091)]
    )
    encourage_girls_nontraditional_careers = pick_weighted(
        [("Yes", 0.848), ("Maybe", 0.101), ("No", 0.051)]
    )
    man_final_say_home = pick_weighted(
        [("No", 0.667), ("Yes", 0.182), ("Maybe", 0.152)]
    )

    # --- 4.6 Gender-based Violence ---------------------------------------------
    woman_should_not_tolerate_violence = pick_weighted(
        [("No", 0.704), ("Yes", 0.245), ("Maybe", 0.051)]
    )
    sexist_jokes_okay = pick_weighted(
        [("No", 0.768), ("Yes", 0.172), ("Maybe", 0.06)]
    )
    sensitization_reduces_violence = pick_weighted(
        [("Yes", 0.633), ("No", 0.143), ("Maybe", 0.224)]
    )

    rows.append(
        {
            "respondent_id": respondent_id,
            "age_group": age_group,
            "gender": gender,
            "education": education,
            "heard_term_gender_sensitization": "Yes" if heard_term else "No",
            "gender_is_binary_or_nonbinary": gender_binary_view,
            "received_gender_training": "Yes" if received_training else "No",
            "support_school_curriculum": support_school_curriculum,
            "support_workplace_training": support_workplace_training,
            "sensitization_helps_relationships": sensitization_helps_relationships,
            "stereotype_men_rational_women_emotional": men_rational_women_emotional,
            "stereotype_women_bad_drivers": women_bad_drivers,
            "view_okay_for_men_to_cry": okay_for_men_to_cry,
            "stereotype_men_strong_women_weak": men_strong_women_weak,
            "stereotype_women_better_housekeepers": women_better_housekeepers,
            "stereotype_gentle_men_less_masculine": gentle_men_less_masculine,
            "childcare_only_mother_responsibility": childcare_only_mother,
            "woman_home_man_provider_role": woman_home_man_provider,
            "chores_divided_equally_if_both_work": chores_divided_equally,
            "support_equal_pay": equal_pay_support,
            "support_gender_diversity_quotas": gender_diversity_support,
            "discrimination_observed_nearby": discrimination_observed,
            "support_gender_neutral_language": gender_neutral_language_support,
            "encourage_girls_nontraditional_careers": encourage_girls_nontraditional_careers,
            "man_should_have_final_say_home": man_final_say_home,
            "woman_should_not_tolerate_violence": woman_should_not_tolerate_violence,
            "sexist_jokes_okay": sexist_jokes_okay,
            "sensitization_reduces_gbv": sensitization_reduces_violence,
        }
    )

# ---------------------------------------------------------------------------
# Write CSV
# ---------------------------------------------------------------------------
fieldnames = list(rows[0].keys())

with open("synthetic_survey.csv", "w", newline="", encoding="utf-8") as f:
    writer = csv.DictWriter(f, fieldnames=fieldnames)
    writer.writeheader()
    writer.writerows(rows)

print(f"Generated {len(rows)} synthetic rows -> synthetic_survey.csv")

# ---------------------------------------------------------------------------
# Quick sanity check: print a few aggregate stats vs. published targets
# ---------------------------------------------------------------------------
def pct(field, value):
    return 100 * sum(1 for r in rows if r[field] == value) / len(rows)

print("\nSanity check (synthetic vs. published target):")
print(f"  Heard term 'Yes': {pct('heard_term_gender_sensitization', 'Yes'):.1f}% (target 77%)")
print(f"  Received training 'Yes': {pct('received_gender_training', 'Yes'):.1f}% (target ~39.8%)")
print(f"  Equal pay support 'Yes': {pct('support_equal_pay', 'Yes'):.1f}% (target 90.8%)")
print(f"  Okay for men to cry 'Agree': {pct('view_okay_for_men_to_cry', 'Agree'):.1f}% (target 82%)")
print(f"  Gender diversity support 'Yes': {pct('support_gender_diversity_quotas', 'Yes'):.1f}% (target 70.4%)")
